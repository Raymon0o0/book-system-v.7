    private void addDescriptionToBook(String title, String description, String authorBio,
                                      String publisher, String isbn, Integer pages) {
        try {
            List<Book> books = bookRepository.findByTitleContainingIgnoreCase(title);
            if (!books.isEmpty()) {
                Book book = books.get(0);
                if (book.getDescription() == null || book.getDescription().isEmpty()) {
                    book.setDescription(description);
                    book.setAuthorBio(authorBio);
                    book.setPublisher(publisher);
                    book.setIsbn(isbn);
                    book.setPages(pages);
                    bookRepository.save(book);
                    System.out.println("✓ Добавлено описание для: " + title);
                }
            } else {
                System.out.println("⚠️ Книга не найдена: " + title);
            }
        } catch (Exception e) {
            System.out.println("⚠️ Ошибка при добавлении описания для: " + title + " - " + e.getMessage());
        }
    }

